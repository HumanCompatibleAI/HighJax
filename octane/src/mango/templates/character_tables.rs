//! Character lookup tables for quadrant, sextant, and octant Unicode block elements.

/// Get the quadrant character for a 4-bit pattern.
#[allow(dead_code)]
pub fn quadrant_char(pattern: u8) -> char {
    debug_assert!(pattern < 16, "Quadrant pattern must be 0-15");
    QUADRANT_CHARS[pattern as usize]
}

/// Get the sextant character for a 6-bit pattern.
#[allow(dead_code)]
pub fn sextant_char(pattern: u8) -> char {
    debug_assert!(pattern < 64, "Sextant pattern must be 0-63");
    SEXTANT_CHARS[pattern as usize]
}

/// Get the octant character for an 8-bit pattern.
#[allow(dead_code)]
pub fn octant_char(pattern: u8) -> char {
    OCTANT_CHARS[pattern as usize]
}

